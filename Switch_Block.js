var fruit = "Strawberry";
switch (fruit){
    case "Banana":
    console.log("Bananas are yellow.");
    break;
    case "Orange":
    console.log("Oranges are orange.");
    break;
    case "Strawberry":
    console.log("Strawberries are red.");
    break;
    case "Lime":
    console.log("Limes are green.");       
    break;
    default:
    console.log("Sorry I don't know what color that fruit is.");
}    